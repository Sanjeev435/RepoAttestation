	
CREATE TABLE T_ADMIN_DETAIL (
ADMIN_ID NUMERIC(9,0),
LOGIN_ID VARCHAR(50) NOT NULL,
CONSTRAINT PK_ADMIN_DETAIL PRIMARY KEY (ADMIN_ID)
);

CREATE SEQUENCE S_ADMIN_DETAIL INCREMENT BY 1 MAXVALUE 999999999 START WITH 1;


CREATE TABLE T_PROJECT_DETAIL (
PROJECT_ID NUMERIC(9,0),
PROJECT_NAME VARCHAR(100) NOT NULL,
REPO_URL VARCHAR(200),
CONSTRAINT PK_PROJECT_DETAIL PRIMARY KEY (PROJECT_ID)
);

CREATE SEQUENCE S_PROJECT_DETAIL INCREMENT BY 1 MAXVALUE 999999999 START WITH 1;

CREATE TABLE T_ATTESTATION_DETAIL ( 
ATTESTATION_ID NUMERIC(9,0),
EMP_ID VARCHAR(10) NOT NULL,
EMAIL VARCHAR(100) NOT NULL,
CONSTRAINT PK_ATTESTATION_DETAIL PRIMARY KEY (ATTESTATION_ID));

CREATE SEQUENCE S_ATTESTATION_DETAIL INCREMENT BY 1 MAXVALUE 999999999 START WITH 1;


