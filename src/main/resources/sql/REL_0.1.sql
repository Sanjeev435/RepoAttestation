CONSTRAINT FK_ATT_AUDIT_DETAIL FOREIGN KEY (ATTESTATION_ID) REFERENCES T_ATTESTATION_DETAIL (ATTESTATION_ID),
CONSTRAINT FK_PROJECT_DETAIL FOREIGN KEY (PROJECT_ID) REFERENCES T_PROJECT_DETAIL (PROJECT_ID)
);

CREATE SEQUENCE S_ATTESTATION_AUD_REF INCREMENT BY 1 MAXVALUE 999999999 START WITH 1;

